numbers = [10,20,30,40,50]
print(numbers)
# slicing list[start:end:step]
print(numbers[0:3])
print(numbers[:3])
print(numbers[:])
print(numbers[1:])
print(numbers[1::])
print(numbers[1::1])
print(numbers[1::2])
print(numbers[1::22])
print(numbers[len(numbers)-1])

# Reverse the list
print(numbers[::-1])

print(numbers[-3:])
